                    new decimal[] { -1, 0, 0 },
                    new decimal[] { 3, -2, 0 },
                    new decimal[] { -2, 1, 4 }
                }
            ),
            new decimal[] { 1, -7, 8 }
        );
    }

    static void SolveBackwardSystems()
    {
        var solver = new BackwardSubstitutionSolver();

        RunOutput
        (
            solver,
            new SquareMatrix(
                new decimal[][]
                {
                    new decimal[] { 1, 3, 4 },
                    new decimal[] { 0, -2, 2 },
                    new decimal[] { 0, 0, 4 }
                }
            ),
            new decimal[] { 11, -2, 4 }
        );

        RunOutput
        (
            solver,
            new SquareMatrix(
                new decimal[][]
                {
                    new decimal[] { 1, 2, -4 },
                    new decimal[] { 0, -2, -1 },
                    new decimal[] { 0, 0, 3 }
                }
            ),
            new decimal[] { 7, -7, 9 }
        );
    }

    private static void SolveWithGaussElimination()
    {
        var solver = new GaussEliminationSolver();

        RunOutput
        (
            solver,
            new SquareMatrix(
                new decimal[][]
                {
                    new decimal[] { 1, 2, 1, -1 },
                    new decimal[] { 3, 2, 4, 4 },
                    new decimal[] { 4, 4, 3, 4 },
                    new decimal[] { 2, 0, 1, 5 },
                }
            ),
            new decimal[] { 5, 16, 22, 15 }
        );

        RunOutput
        (
            solver,
            new SquareMatrix(
                new decimal[][]
                {
                    new decimal[] { 1, 2, 2 },
                    new decimal[] { 4, 4, 2 },
                    new decimal[] { 4, 6, 4 }
                }
            ),
            new decimal[] { 3, 6, 10 }
        );
    }

    static private void RunOutput(ISolver solver, SquareMatrix matrix, decimal[] parameters)
    {
        Console.WriteLine($"Running {solver.GetType().Name}");
        Console.WriteLine($"Parameters: {string.Join(",", parameters)}");
        Console.WriteLine(matrix.ToString());

        var solvedValues = solver.Solve(matrix, parameters);
        Console.WriteLine($"Solved values: {string.Join(",", solvedValues)}");
        Console.WriteLine();
    }

